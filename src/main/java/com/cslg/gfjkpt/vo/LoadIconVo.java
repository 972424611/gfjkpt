    public void setMaxMouthLoad(double maxMouthLoad) {
        this.maxMouthLoad = maxMouthLoad;
    }

    public double getMaxYearLoad() {
        return maxYearLoad;
    }

    public void setMaxYearLoad(double maxYearLoad) {
        this.maxYearLoad = maxYearLoad;
    }
