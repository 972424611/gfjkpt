package com.cslg.gfjkpt.vo.inverter;

public class IconVo {

    private double currentOutput;

    private double irradiance;

    private double temperature;

    private double co2Reduction;

    private double equivalentTree;

    private double totalIncome;

    public double getCurrentOutput() {
        return currentOutput;
    }

    public void setCurrentOutput(double currentOutput) {
        this.currentOutput = currentOutput;
    }

    public double getIrradiance() {
        return irradiance;
    }

    public void setIrradiance(double irradiance) {
        this.irradiance = irradiance;
    }

    public double getTemperature() {
        return temperature;
    }

    public void setTemperature(double temperature) {
        this.temperature = temperature;
    }

    public double getCo2Reduction() {
        return co2Reduction;
    }

    public void setCo2Reduction(double co2Reduction) {
        this.co2Reduction = co2Reduction;
    }

    public double getEquivalentTree() {
        return equivalentTree;
    }

    public void setEquivalentTree(double equivalentTree) {
        this.equivalentTree = equivalentTree;
    }

    public double getTotalIncome() {
        return totalIncome;
    }

    public void setTotalIncome(double totalIncome) {
        this.totalIncome = totalIncome;
    }
}
